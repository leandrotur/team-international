from src.datacapture import DataCapture


def test_less():
    capture = DataCapture()
    capture.add(3)
    capture.add(9)
    capture.add(3)
    capture.add(4)
    capture.add(6)
    stats = capture.build_stats()
    result = stats.less(4)
    assert result == 2


